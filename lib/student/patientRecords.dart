class PatientRecordInfo {
  final int? id;
  final int? patientID;
  final int? studentID;
  final String? lastDisease;
  final String? currentDisease;
  final String? generalDisease;

  PatientRecordInfo({
    required this.id,
    required this.patientID,
    required this.studentID,
    required this.lastDisease,
    required this.currentDisease,
    required this.generalDisease,
  });
}
